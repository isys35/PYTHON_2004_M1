num = int(input("Введите число от 1 до 7:"))
if num == 1:
    print("Понедельник")
elif num == 2:
    print("Вторник")
elif num == 3:
    print("Среда")
elif num == 4:
    print("Четверг")
elif num == 5:
    print("Пятница")
elif num == 6:
    print("Суббота")
elif num == 7:
    print("Четверг")
else:
    print("Ошибка: введите число от 1 до 7")